#include <stdio.h>

/**
 * int add(int a, int b) - Returns the sum of two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The sum of a and b.
 */
int add(int a, int b)
{
    return (a + b);
}

/**
 * int sub(int a, int b) - Returns the difference between two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The difference between a and b.
 */
int sub(int a, int b)
{
    return (a - b);
}

/**
 * int mul(int a, int b) - Returns the product of two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The product of a and b.
 */
int mul(int a, int b)
{
    return (a * b);
}

/**
 * int div(int a, int b) - Returns the quotient of two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The quotient of a and b.
 */
int divide(int a, int b)
{
    return (a / b);
}

/**
 * int mod(int a, int b) - Returns the remainder of the division of two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The remainder of the division of a by b.
 */
int mod(int a, int b)
{
    return (a % b);
}
